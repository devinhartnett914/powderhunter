#!/usr/bin/env node
// Emit a SQL file Devin can run in the Supabase SQL editor to insert the new international roster.
import { readFileSync, writeFileSync } from "node:fs";

const inserts = JSON.parse(readFileSync("data/scraped/to-insert.json", "utf8"));
const geo = JSON.parse(readFileSync("data/scraped/geocoded.json", "utf8"));

const esc = (s) => s == null ? "NULL" : `'${String(s).replace(/'/g, "''")}'`;
const num = (n) => n == null ? "NULL" : String(n);

const lines = [];
lines.push("-- 002_insert_international_roster.sql");
lines.push("-- Generated by scripts/generate-upsert-sql.mjs");
lines.push("-- Inserts international + newly-listed destinations scraped from the 5 pass operator sites.");
lines.push("-- Idempotent: uses ON CONFLICT (name, pass_type, location) from migration 001's unique index.");
lines.push("-- URL columns (url, kids_ski_free_url, etc.) are left NULL; scraper MUST NOT overwrite existing non-null URL fields on re-run.");
lines.push("");
lines.push("BEGIN;");
lines.push("");

for (const r of inserts) {
  const key = `${r.pass_type}|${r.name}|${r.location}`;
  const g = geo[key] || {};
  lines.push(
    `INSERT INTO resorts (name, pass_type, location, lat, lng, static_updated_at) VALUES (` +
    `${esc(r.name)}, ${esc(r.pass_type)}, ${esc(r.location)}, ${num(g.lat)}, ${num(g.lng)}, now()` +
    `) ON CONFLICT (name, pass_type, location) DO NOTHING;`
  );
}

lines.push("");
lines.push("COMMIT;");
lines.push("");
writeFileSync("db/migrations/002_insert_international_roster.sql", lines.join("\n"));
console.log(`wrote ${inserts.length} INSERT statements`);

#!/usr/bin/env node
// Emit a SQL file Devin can run in the Supabase SQL editor to upsert the latest roster scrape.
//
// The DB holds one row per mountain, keyed on (lower(name), lower(location)).
// pass_types is a text[] of which passes the mountain is on. When a mountain
// already exists, we union the new pass into pass_types rather than inserting
// a duplicate row.
import { readFileSync, writeFileSync } from "node:fs";

const inserts = JSON.parse(readFileSync("data/scraped/to-insert.json", "utf8"));
const geo = JSON.parse(readFileSync("data/scraped/geocoded.json", "utf8"));

const esc = (s) => s == null ? "NULL" : `'${String(s).replace(/'/g, "''")}'`;
const num = (n) => n == null ? "NULL" : String(n);

const lines = [];
lines.push("-- Generated by scripts/generate-upsert-sql.mjs");
lines.push("-- Upserts scraped roster into `resorts`. Assumes migrations 008+009 have");
lines.push("-- run (pass_types TEXT[] exists, unique index is on (lower(name), lower(location))).");
lines.push("-- Idempotent: ON CONFLICT unions the new pass into pass_types without touching");
lines.push("-- other columns.");
lines.push("");
lines.push("BEGIN;");
lines.push("");

for (const r of inserts) {
  const key = `${r.pass_type}|${r.name}|${r.location}`;
  const g = geo[key] || {};
  lines.push(
    `INSERT INTO resorts (name, location, pass_types, lat, lng, static_updated_at) VALUES (` +
    `${esc(r.name)}, ${esc(r.location)}, ARRAY[${esc(r.pass_type)}]::text[], ${num(g.lat)}, ${num(g.lng)}, now()` +
    `) ON CONFLICT (lower(name), lower(location)) DO UPDATE SET ` +
    `pass_types = (SELECT array(SELECT DISTINCT unnest(resorts.pass_types || EXCLUDED.pass_types))), ` +
    `static_updated_at = now();`
  );
}

lines.push("");
lines.push("COMMIT;");
lines.push("");

const outFile = `db/migrations/${new Date().toISOString().slice(0, 10)}_roster_upsert.sql`;
writeFileSync(outFile, lines.join("\n"));
console.log(`wrote ${inserts.length} UPSERT statements to ${outFile}`);
